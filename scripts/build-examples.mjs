import { readFileSync, writeFileSync } from "fs";

const cet4Path = "src/data/cet4Words.ts";
const content = readFileSync(cet4Path, "utf-8");

const wordPattern = /w\("([^"]+)",\s*"([^"]*)",\s*"([^"]*)"/g;
const words = [];
let match;
while ((match = wordPattern.exec(content)) !== null) {
  words.push({ word: match[1], pos: match[2], zh: match[3] });
}
console.log(`Found ${words.length} CET-4 words`);

function capitalize(s) {
  if (!s) return s;
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function hashCode(str) {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = ((hash << 5) - hash) + str.charCodeAt(i);
    hash |= 0;
  }
  return Math.abs(hash);
}

function fixZhDup(zh) {
  // Remove duplicate 的 at end
  return zh.replace(/的的$/, "的").replace(/的的([^的])/g, "的$1");
}

function generateExample(word, pos, zh) {
  const mainPos = (pos.split("/")[0] || "").trim();
  const zhClean = zh.split(/[；;，,]/)[0].replace(/[（(].*[）)]/g, "").replace(/^[nvadjadv]+\.?\s*/, "").trim();
  const idx = hashCode(word);

  // ---- NOUN ----
  if (mainPos.startsWith("n.") || (!mainPos.startsWith("v.") && !mainPos.startsWith("adj.") && !mainPos.startsWith("adv."))) {
    const templates = [
      { en: `The teacher explained the meaning of ${word} to the class.`, zh: `老师向全班解释了${zhClean}的含义。` },
      { en: `We had a serious discussion about ${word} at the meeting.`, zh: `我们在会议上认真讨论了${zhClean}的问题。` },
      { en: `${capitalize(word)} is a key concept in this area of research.`, zh: `${zhClean}是这个研究领域的一个关键概念。` },
      { en: `The book offers a clear explanation of ${word} for beginners.`, zh: `这本书为初学者清晰地解释了${zhClean}。` },
      { en: `A basic knowledge of ${word} is required for this course.`, zh: `这门课程需要具备${zhClean}的基础知识。` },
      { en: `The article examines the role of ${word} in modern society.`, zh: `这篇文章探讨了${zhClean}在现代社会中的作用。` },
      { en: `She gave a presentation on ${word} at the international conference.`, zh: `她在国际会议上做了一个关于${zhClean}的报告。` },
      { en: `Many experts have studied the impact of ${word} on the economy.`, zh: `许多专家研究了${zhClean}对经济的影响。` },
    ];
    return templates[idx % templates.length];
  }

  // ---- VERB ----
  if (mainPos.startsWith("v.")) {
    const templates = [
      { en: `We need to ${word} the situation before taking any action.`, zh: `在采取任何行动之前，我们需要${zhClean}一下情况。` },
      { en: `She carefully ${word}ed the data before writing the report.`, zh: `她在写报告之前仔细${zhClean}了数据。` },
      { en: `The team is working to ${word} the challenges they are facing.`, zh: `团队正在努力${zhClean}他们面临的挑战。` },
      { en: `He learned to ${word} difficult problems through experience.`, zh: `他通过经验学会了${zhClean}困难的问题。` },
      { en: `It is important to ${word} all the relevant information first.`, zh: `首先${zhClean}所有相关信息是很重要的。` },
      { en: `You should ${word} this opportunity while you still can.`, zh: `你应该趁现在还有机会${zhClean}这个机会。` },
      { en: `The professor asked us to ${word} the theory in our own words.`, zh: `教授让我们用自己的话${zhClean}这个理论。` },
    ];
    return templates[idx % templates.length];
  }

  // ---- ADJECTIVE ----
  if (mainPos.startsWith("adj.")) {
    const templates = [
      { en: `This is a very ${word} issue that needs immediate attention.`, zh: `这是一个非常${zhClean}的问题，需要立即关注。` },
      { en: `The experiment produced some ${word} results last month.`, zh: `上个月的实验产生了一些${zhClean}的结果。` },
      { en: `It is ${word} to think carefully before making big decisions.`, zh: `在做重大决定前仔细思考是${zhClean}的。` },
      { en: `Her ${word} performance earned praise from the entire team.`, zh: `她${zhClean}的表现赢得了全队的赞扬。` },
      { en: `A ${word} environment is essential for children's development.`, zh: `${zhClean}的环境对儿童的发展至关重要。` },
      { en: `He shared some ${word} advice based on his years of experience.`, zh: `他根据多年的经验分享了一些${zhClean}的建议。` },
    ];
    return templates[idx % templates.length];
  }

  // ---- ADVERB ----
  if (mainPos.startsWith("adv.")) {
    const templates = [
      { en: `She explained the rules ${word} so everyone could follow.`, zh: `她${zhClean}地解释了规则，这样每个人都能遵守。` },
      { en: `The team needs to act ${word} to meet the approaching deadline.`, zh: `团队需要${zhClean}地行动以应对即将到来的截止日期。` },
      { en: `He answered the questions ${word} during the press conference.`, zh: `他在新闻发布会上${zhClean}地回答了问题。` },
    ];
    return templates[idx % templates.length];
  }

  return { en: `The word "${word}" is used frequently in academic writing.`, zh: `"${word}"这个词在学术写作中使用频繁。` };
}

// Generate all examples
const lines = [];
lines.push("// Auto-generated example sentences for CET-4 vocabulary");
lines.push("// Generated by scripts/build-examples.mjs");
lines.push("");
lines.push("const wordExamples: Record<string, { en: string; zh: string }> = {");

for (const { word, pos, zh } of words) {
  const ex = generateExample(word, pos, zh);
  const fixedZh = fixZhDup(ex.zh);
  lines.push(`  ${JSON.stringify(word)}: { en: ${JSON.stringify(ex.en)}, zh: ${JSON.stringify(fixedZh)} },`);
}

lines.push("};");
lines.push("");
lines.push("export function getWordExample(word: string): { en: string; zh: string } | null {");
lines.push("  return wordExamples[word.toLowerCase()] || null;");
lines.push("}");
lines.push("");
lines.push("export default wordExamples;");

writeFileSync("src/data/examples.ts", lines.join("\n"), "utf-8");
console.log(`Generated ${words.length} examples → src/data/examples.ts`);
